# In-memory explanation cache
COPILOT_EXPLAIN_CACHE = {}

@app.route('/api/copilot/explain', methods=['POST'])
@login_required
def api_copilot_explain():
    data = request.get_json() or {}
    node_id = data.get('node_id')
    detection_id = data.get('detection_id')
    
    if not node_id and not detection_id:
        return jsonify({'success': False, 'message': 'Missing node_id or detection_id'}), 400
        
    summary = None
    if detection_id:
        summary = get_node_summary_for_detection(detection_id)
        
    if not summary and node_id:
        summary = get_node_summary(node_id)
        
    if not summary or not summary['metrics']:
        return jsonify({'success': False, 'message': f'No telemetry available for requested entity'}), 404
        
    resolved_node_id = summary['metadata']['node_id']
    timestamp = summary['metrics']['timestamp']
    cache_key = f"{resolved_node_id}_{timestamp}"
    
    # Check cache to prevent redundant LLM queries
    if cache_key in COPILOT_EXPLAIN_CACHE:
        return jsonify(COPILOT_EXPLAIN_CACHE[cache_key])
        
    decision = compute_decision_layer(resolved_node_id, summary['metrics'])
    narration = generate_narration("node_explain", summary, decision)
    
    result = {
        'success': True,
        'metadata': summary['metadata'],
        'metrics': summary['metrics'],
        'decision': decision,
        'narration': narration
    }
    
    # Cache result
    COPILOT_EXPLAIN_CACHE[cache_key] = result
    return jsonify(result)

@app.route('/api/copilot/chat', methods=['POST'])
@login_required
def api_copilot_chat():
    data = request.get_json() or {}
    message = data.get('message', '').strip()
    context_node_id = data.get('context_node_id')
    cloud_context = data.get('cloud_context', 'Multi-Cloud')
    history = data.get('history', [])
    
    if not message:
        return jsonify({'success': False, 'message': 'Empty message'}), 400
        
    intent_type, ref_node_id, resolved_data = classify_and_route_intent(message, cloud_context)
    
    # If conversational follow-up (context tracking)
    if intent_type == "general_help" and context_node_id:
        msg_lower = message.lower()
        # Only intercept if it's actually asking about the context node
        node_keywords = ['why', 'explain', 'reason', 'status', 'threat', 'this', 'action', 'do', 'remedi', 'fix', 'mitigat', context_node_id.lower()]
        if any(w in msg_lower for w in node_keywords) and not any(g in msg_lower for g in ['python', 'docker', 'kubernetes', 'k8s', 'loop', 'programming', 'binary tree', 'sorting', 'algorithm', 'quicksort', 'what is cloud']):
            summary = get_node_summary(context_node_id)
            if summary and summary['metrics']:
                decision = compute_decision_layer(context_node_id, summary['metrics'])
                if any(x in msg_lower for x in ['action', 'do', 'remedi', 'fix', 'mitigat']):
                    response_text = f"For **{context_node_id}** ({summary['metrics']['threat_type']}), the recommended security playbooks suggest:\n\n"
                    response_text += "\n".join([f"- {a}" for a in decision['recommended_actions']])
                    response_text += "\n\nPlease apply these controls to isolate the host system immediately."
                else:
                    response_text = f"Node **{context_node_id}** is currently flagged with **{summary['metrics']['threat_type']}** because:\n\n"
                    response_text += "\n".join([f"- {r}" for r in decision['reasons']])
                return jsonify({'success': True, 'response': response_text, 'node_id': context_node_id})
            
    # Standard intent routing
    payload = {
        'intent': intent_type,
        'node_id': ref_node_id,
        'data': resolved_data,
        'message': message,
        'history': history
    }
    
    response_text = generate_narration("chat_query", payload)
    return jsonify({
        'success': True,
        'response': response_text,
        'node_id': ref_node_id if ref_node_id else context_node_id
    })

