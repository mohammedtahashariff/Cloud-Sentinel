@app.route('/api/detections', methods=['GET'])
@login_required
def api_detections():
    page = int(request.args.get('page', 1))
    limit = int(request.args.get('limit', 15))
    category = request.args.get('category')
    is_anomalous = request.args.get('is_anomalous')
    cloud_provider = request.args.get('cloud_provider')
    resource_type = request.args.get('resource_type')
    
    offset = (page - 1) * limit
    
    # Helper function to extract node_id from event_id
    def parse_node_id(event_id):
        if not event_id:
            return 'unknown'
        parts = event_id.split('-')
        if len(parts) >= 3:
            if parts[-1].isdigit() and parts[-2].isdigit():
                return '-'.join(parts[1:-2])
            else:
                return '-'.join(parts[1:])
        return event_id

    conn = get_db_connection()
    
    # Load all nodes into a quick-lookup dict
    nodes_rows = conn.execute("SELECT node_id, cloud_provider, resource_type, ip, region FROM nodes").fetchall()
    nodes_dict = {
        r['node_id']: {
            'node_id': r['node_id'],
            'cloud_provider': r['cloud_provider'],
            'resource_type': r['resource_type'],
            'ip': r['ip'],
            'region': r['region']
        } for r in nodes_rows
    }
    
    # Query all raw detections
    query = "SELECT detection_id, event_id, category, anomaly_score, is_anomalous, timestamp, raw_log FROM detections WHERE 1=1"
    params = []
    
    if category:
        query += " AND category = ?"
        params.append(category)
    if is_anomalous is not None and is_anomalous != '':
        query += " AND is_anomalous = ?"
        params.append(int(is_anomalous))
        
    query += " ORDER BY timestamp DESC"
    all_raw_detections = conn.execute(query, params).fetchall()
    
    # Process and filter in Python to prevent pruning data losses
    filtered_detections = []
    for r in all_raw_detections:
        node_id = parse_node_id(r['event_id'])
        node_info = nodes_dict.get(node_id, {
            'node_id': node_id,
            'cloud_provider': 'AWS',
            'resource_type': 'VM',
            'ip': '10.0.1.10',
            'region': 'ap-southeast-2'
        })
        
        # Apply filters
        if cloud_provider and node_info['cloud_provider'] != cloud_provider:
            continue
        if resource_type and node_info['resource_type'] != resource_type:
            continue
            
        filtered_detections.append({
            'detection_id': r['detection_id'],
            'event_id': r['event_id'],
            'category': r['category'],
            'anomaly_score': round(r['anomaly_score'], 4),
            'is_anomalous': bool(r['is_anomalous']),
            'timestamp': r['timestamp'],
            'raw_log': r['raw_log'],
            'node_id': node_info['node_id'],
            'cloud_provider': node_info['cloud_provider'],
            'resource_type': node_info['resource_type'],
            'ip': node_info['ip'],
            'region': node_info['region']
        })
        
    total_count = len(filtered_detections)
    paginated_detections = filtered_detections[offset:offset+limit]
    
    # Fetch telemetry logs for nodes (last 50 for time series plotting)
    chart_metrics = conn.execute("""
        SELECT timestamp, 
               MAX(risk_score) as risk_score, 
               MAX(is_anomalous) as is_anomalous, 
               AVG(cpu_utilization) as cpu_utilization, 
               AVG(memory_utilization) as memory_utilization, 
               'System' as threat_type, 
               'system-summary' as node_id
        FROM node_metrics
        GROUP BY timestamp
        ORDER BY timestamp DESC LIMIT 30
    """).fetchall()
    
    # Calculate counts per category (last 30 days of actual detections)
    cat_counts = conn.execute("""
        SELECT category as threat_type, COUNT(*) as count 
        FROM detections 
        WHERE timestamp >= datetime('now', '-30 days')
        GROUP BY category
    """).fetchall()
    
    # Generate the last 7 dates ending today
    import datetime as dt_mod
    today = dt_mod.date.today()
    dates_str = [(today - dt_mod.timedelta(days=i)).strftime('%Y-%m-%d') for i in range(6, -1, -1)]
    
    daily_high = {d: 0 for d in dates_str}
    daily_med = {d: 0 for d in dates_str}
    daily_low = {d: 36 for d in dates_str}
    
    # Query database for daily detections in the last 7 days without joining pruned metrics
    daily_detections_rows = conn.execute("""
        SELECT date(timestamp) as dt, event_id, MAX(anomaly_score) as max_score
        FROM detections
        WHERE timestamp >= date('now', '-7 days')
        GROUP BY dt, event_id
    """).fetchall()
    
    daily_node_scores = {d: {} for d in dates_str}
    for r in daily_detections_rows:
        dt = r['dt']
        if dt in daily_node_scores:
            n_id = parse_node_id(r['event_id'])
            daily_node_scores[dt][n_id] = max(daily_node_scores[dt].get(n_id, 0), r['max_score'])
            
    for d in dates_str:
        high = 0
        med = 0
        for node_id, score in daily_node_scores[d].items():
            if score >= 0.8:
                high += 1
            elif score >= 0.15:
                med += 1
        daily_high[d] = high
        daily_med[d] = med
        daily_low[d] = max(0, 36 - high - med)
        
    daily_stats = []
    for d in dates_str:
        daily_stats.append({
            'date': d,
            'high': daily_high[d],
            'medium': daily_med[d],
            'low': daily_low[d]
        })
        
    conn.close()
    
    chart_data = []
    for c in chart_metrics:
        chart_data.append({
            'timestamp': c['timestamp'],
            'risk_score': c['risk_score'],
            'is_anomalous': c['is_anomalous'],
            'cpu': c['cpu_utilization'],
            'ram': c['memory_utilization'],
            'threat_type': c['threat_type'],
            'node_id': c['node_id']
        })
        
    categories = [{'category': c['threat_type'], 'count': c['count']} for c in cat_counts]
        
    return jsonify({
        'detections': paginated_detections,
        'total': total_count,
        'page': page,
        'limit': limit,
        'pages': int(np.ceil(total_count / limit)) if limit > 0 else 0,
        'chart_data': chart_data,
        'categories': categories,
        'daily_stats': daily_stats
    })

